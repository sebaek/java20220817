package ch04.lecture;

public class C09For {
	public static void main(String[] args) {
		System.out.println("======================");
		for (int i = 0; i < 5; i++) {
			for (int j = 0; j < 5; j++) {
				System.out.print(j);
			}
			System.out.println();
		}
		
		System.out.println("=====================");
		
	}
}
